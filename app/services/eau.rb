def trig_bas_eau(trig)
  if trig == 'ciel'
    '6 - Plaider sa cause'
  elsif trig == 'tonnerre'
    '40 - Délivrance'
  elsif trig == 'montagne'
    '4 - Jeune Fou'
  elsif trig == 'marais'
    '59 - Dénouer'
  elsif trig == 'brume'
    '47 - Epuisement'
  elsif trig == 'eau'
    '29 - Ravins'
  elsif trig == 'feu'
    '64 - Pas encore traversée'
  elsif trig == 'terre'
    '7 - Armée'
  end
end
