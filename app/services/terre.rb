def trig_bas_terre(trig)
  if trig == 'ciel'
    '12 - Adversité'
  elsif trig == 'tonnerre'
    "16 - S'enthousiasmer"
  elsif trig == 'montagne'
    '23 - Usure'
  elsif trig == 'marais'
    '20 - Regarder'
  elsif trig == 'brume'
    '45 - Réunion'
  elsif trig == 'eau'
    '8 - Alliance'
  elsif trig == 'feu'
    '35 - Avancer au Grand Jour'
  elsif trig == 'terre'
    '2 - Elan Réceptif'
  end
end
