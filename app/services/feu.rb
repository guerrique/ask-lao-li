def trig_bas_feu(trig)
  if trig == 'ciel'
    "13 - S'entendre avec tous"
  elsif trig == 'tonnerre'
    '55 - Abondance'
  elsif trig == 'montagne'
    '22 - Embellir'
  elsif trig == 'marais'
    '37 - Gens du Clan'
  elsif trig == 'brume'
    '49 - Révolution'
  elsif trig == 'eau'
    '63 - Déjà traversée'
  elsif trig == 'feu'
    "30 - Filet d'oiseleur"
  elsif trig == 'terre'
    '36 - Lumière Obscurcie'
  end
end
