def trig_bas_tonnerre(trig)
  if trig == 'ciel'
    '25 - Spontanément'
  elsif trig == 'tonnerre'
    '51 - Ebranler'
  elsif trig == 'montagne'
    '27 - Nourrir'
  elsif trig == 'marais'
    '42 - Augmenter'
  elsif trig == 'brume'
    '17 - Suivre'
  elsif trig == 'eau'
    '3 - Difficultés Initiales'
  elsif trig == 'feu'
    '21 - Mordre et Unir'
  elsif trig == 'terre'
    '24 - Retour'
  end
end
