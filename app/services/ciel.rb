def trig_bas_ciel(trig)
  if trig == 'ciel'
    '1 - Elan Créatif'
  elsif trig == 'tonnerre'
    '34 - Grand Force'
  elsif trig == 'montagne'
    '26 - Grand Apprivoise'
  elsif trig == 'marais'
    '9 - Petit Apprivoise'
  elsif trig == 'brume'
    '43 - Se Montrer Résolu'
  elsif trig == 'eau'
    '5 - Attendre'
  elsif trig == 'feu'
    '14 - Grand Réalise'
  elsif trig == 'terre'
    '11 - Prospérité'
  end
end
