def trig_bas_bois(trig)
  if trig == 'ciel'
    '44 - Etre Accueillant'
  elsif trig == 'tonnerre'
    '32 - Endurer'
  elsif trig == 'montagne'
    '18 - Remédier au Corrompu'
  elsif trig == 'bois'
    '57 - Se Modeler'
  elsif trig == 'brume'
    '28 - Grand Excès'
  elsif trig == 'eau'
    '48 - Puits'
  elsif trig == 'feu'
    '50 - Chaudron'
  elsif trig == 'terre'
    '46 - Croissance'
  end
end
