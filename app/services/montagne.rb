def trig_bas_montagne(trig)
  if trig == 'ciel'
    '33 - Retraite'
  elsif trig == 'tonnerre'
    '62 - Petit Excès'
  elsif trig == 'montagne'
    '52 - Stabiliser'
  elsif trig == 'marais'
    '53 - Pas à Pas'
  elsif trig == 'brume'
    '31 - Inciter'
  elsif trig == 'eau'
    '39 - Obstruction'
  elsif trig == 'feu'
    '56 - Voyageur'
  elsif trig == 'terre'
    '15 - Se Tenir'
  end
end
